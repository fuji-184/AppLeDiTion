        Penyakit Cedar Apple Rust adalah penyakit jamur yang disebabkan oleh patogen Gymnosporangium juniperi-virginianae.

        Penyakit Cedar Apple Rust terutama mempengaruhi pohon apel dan tanaman terkait. Berikut adalah penjelasan lebih detail mengenai dampak penyakit ini pada pohon apel:

### Gejala pada Pohon Apel

1. **Daun:**
   - **Bercak-bercak Kuning:** Gejala awal biasanya dimulai dengan munculnya bintik-bintik kecil berwarna kuning pada daun.
   - **Perkembangan Bercak:** Bintik-bintik kuning ini kemudian membesar dan berubah menjadi oranye atau merah dengan pusat yang lebih gelap.
   - **Pembentukan Aecia:** Pada sisi bawah daun yang terkena, akan muncul struktur tubular yang disebut aecia. Aecia ini adalah tempat di mana spora baru dihasilkan dan siap disebarkan kembali ke pohon cedar.
   - **Kematian Daun:** Daun yang parah terinfeksi akan mengering, layu, dan rontok lebih awal dari biasanya.

2. **Buah:**
   - **Bercak Kecil:** Pada buah apel yang terinfeksi, muncul bercak-bercak kecil berwarna kuning atau oranye.
   - **Perkembangan Bercak:** Bercak-bercak ini dapat membesar dan menyebabkan deformasi buah, sehingga menurunkan kualitas buah secara keseluruhan.
   - **Retak dan Busuk:** Pada infeksi yang parah, bercak-bercak ini dapat menyebabkan buah retak dan menjadi rentan terhadap infeksi sekunder oleh patogen lain, yang dapat menyebabkan buah membusuk.

3. **Cabang dan Ranting:**
   - **Gall:** Pada cabang dan ranting muda, infeksi dapat menyebabkan pembentukan gall atau bengkak berwarna coklat kemerahan. Gall ini bisa bertahan selama beberapa tahun dan merupakan sumber utama spora yang menginfeksi tanaman cedar.

### Siklus Hidup Penyakit Cedar Apple Rust

1. **Siklus Tahunan:**
   - **Awal Musim Semi:** Spora dari jamur ini dilepaskan dari gall yang terbentuk pada pohon cedar dan terbawa angin ke pohon apel.
   - **Infeksi Pada Apel:** Spora ini menempel pada daun, buah, atau ranting pohon apel dan menyebabkan infeksi, yang kemudian menghasilkan struktur aecia pada daun apel.
   - **Penyebaran Spora Kembali ke Cedar:** Spora yang dihasilkan dari aecia pada daun apel ini kemudian terbawa angin kembali ke pohon cedar, menyelesaikan siklus hidup jamur.

2. **Faktor Lingkungan:**
   - **Kelembapan:** Penyakit ini sangat bergantung pada kondisi kelembapan tinggi, terutama pada awal musim semi dan akhir musim panas.
   - **Suhu:** Suhu sekitar 15-25Â°C mendukung perkembangan optimal penyakit ini.

### Pengendalian Penyakit

1. **Praktik Budidaya:**
   - **Jarak Tanam:** Menjaga jarak yang cukup antara pohon cedar dan pohon apel untuk mengurangi penyebaran spora.
   - **Pemangkasan:** Pemangkasan cabang yang terinfeksi dan pembuangan daun yang jatuh untuk mengurangi sumber infeksi.

2. **Fungisida:**
   - **Aplikasi Fungisida:** Menggunakan fungisida yang efektif terhadap jamur penyebab Cedar Apple Rust. Aplikasi harus dilakukan sesuai dengan rekomendasi, biasanya sebelum gejala muncul atau saat kondisi lingkungan mendukung perkembangan penyakit.

3. **Varietas Tahan Penyakit:**
   - **Pemilihan Varietas:** Menanam varietas apel yang tahan terhadap Cedar Apple Rust dapat mengurangi risiko infeksi dan kerusakan.

Dengan memahami siklus hidup dan gejala penyakit ini, serta menerapkan langkah-langkah pengendalian yang tepat, petani dapat mengelola dan meminimalkan dampak Cedar Apple Rust pada tanaman apel mereka.
